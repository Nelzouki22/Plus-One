def plus_one(digits):
    # بدء من الرقم الأخير في المصفوفة
    for i in range(len(digits) - 1, -1, -1):
        # إذا كان الرقم أقل من 9، يمكن زيادة الرقم بمقدار 1
        if digits[i] < 9:
            digits[i] += 1
            return digits
        # إذا كان الرقم يساوي 9، يتم تحويله إلى 0
        digits[i] = 0
    
    # إذا كانت جميع الأرقام 9، نحتاج إلى إضافة 1 في بداية المصفوفة
    return [1] + digits

# أمثلة على الاستخدام
print(plus_one([1, 2, 3]))  # الناتج: [1, 2, 4]
print(plus_one([4, 3, 2, 1]))  # الناتج: [4, 3, 2, 2]
print(plus_one([9]))  # الناتج: [1, 0]

